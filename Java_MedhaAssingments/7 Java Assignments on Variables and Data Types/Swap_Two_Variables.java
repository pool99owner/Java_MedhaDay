public class Swap_Two_Variables {
    
    public static void main(String[] args) {
        
        int a = 1;
        int b = 2; 
        int c = 0;

        System.out.println("Before Swapping:- "+"a="+a +" "+"b="+ b+" ");

        
        c=a;
        a=b;
        b=c;
  
        
        System.out.println("After Swapping:- "+"a="+a +" "+"b="+ b+" ");



    }
}
